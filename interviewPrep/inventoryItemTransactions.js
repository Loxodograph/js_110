/*
Write a function that takes two arguments, an inventory item
ID and a list of transactions, and returns an array containing
only the transactions for the specified inventory item
- create a function which takes an inventory ID and an array of objects
- The inventory Id will be filtered to new array containing object.
*/

function transactionsFor(id, object) {
  return object.filter(element => element["id"] === id);
}

let transactions = [ { id: 101, movement: 'in',  quantity:  5 },
  { id: 105, movement: 'in',  quantity: 10 },
  { id: 102, movement: 'out', quantity: 17 },
  { id: 101, movement: 'in',  quantity: 12 },
  { id: 103, movement: 'out', quantity: 20 },
  { id: 102, movement: 'out', quantity: 15 },
  { id: 105, movement: 'in',  quantity: 25 },
  { id: 101, movement: 'out', quantity: 18 },
  { id: 102, movement: 'in',  quantity: 22 },
  { id: 103, movement: 'out', quantity: 15 }, ];

console.log(transactionsFor(101, transactions));
